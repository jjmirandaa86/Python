# coleccion sin indices

color = {"roja", "amarillo", "verde"}
print (color)
print (type(color))

print("roja" in color) # verdad o falso
print (color)

color.add("negro") # agrego
print (color)

color.remove("roja") # remueve roja
print (color)

color.clear() # limpia todo
print (color)
